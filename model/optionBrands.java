package model;

public class optionBrands {
    private int id;
    private String brandName;

    public optionBrands(int id, String brandName) {
        this.id = id;
        this.brandName = brandName;
    }

    public int getId() {
        return id;
    }

    public String getBrandName() {
        return brandName;
    }
    
}
