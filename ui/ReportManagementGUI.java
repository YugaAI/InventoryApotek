    private void btnRiviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRiviewActionPerformed
        // TODO add your handling code here:
        Connection conn = DatabaseConfig.connect();
        supplierRepo supplierRepo = new supplierRepo(conn);
        LogStockRepo LogStockRepo = new LogStockRepo(conn);
        transactionsRepo transactionsRepo = new transactionsRepo(conn);
        
        String kategori = cbKatExport.getSelectedItem().toString();
        int periode = Integer.parseInt(textFieldDate.getText().trim());
        String search = "";
        
        List<?> data = null;
        
        try {
            switch (kategori) {
                case "STOCK MASUK":
                    data = LogStockRepo.getList("stock_in", periode);
                    break;
                case "STOCK KELUAR":
                    data = LogStockRepo.getList("stock_out", periode);
                    break;
                case "MASTER SUPPLIER":
                    data = supplierRepo.listSupplier(search, periode);
                    break;
                case "DATA TRANSAKSI":
                    data = transactionsRepo.getAllTransaction(periode);
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Silakan pilih kategori terlebih dahulu.");
                    return;
            }

            preview review = new preview(kategori, periode, data);
            review.setVisible(true);
        }catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRiviewActionPerformed
    
