    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
      DefaultTableModel model = (DefaultTableModel) tItem.getModel();
      int rowCount = model.getRowCount();
      int colCount = model.getColumnCount();
      
      if (rowCount == 0){
          JOptionPane.showMessageDialog(this, "tidak ada item untuk di simpan", "Error", JOptionPane.ERROR_MESSAGE);
          return;
      }
      
      transaction trx = new transaction();
      trx.setDate(LocalDate.now());
      trx.setGrandTotal(Double.parseDouble(labelGrandTotal.getText()));
      trx.setCreatedAt(LocalDateTime.now());
      trx.setTotalItem(rowCount);
      trx.setCreatedBy(1);
      
      Integer trxId = transactionUc.createTransaction(trx);
      if (trxId == 0) {
        JOptionPane.showMessageDialog(this, "gagal menyimpan transaksi", "Error", JOptionPane.ERROR_MESSAGE);
        return;
      }

        for (int i = 0; i < rowCount; i++) {
         System.out.println("Row ke-" + (i + 1));
         
         TransactionDetail td = new TransactionDetail();
         td.setTransactionId(trxId);
         td.setItemId(itemMap.get(model.getValueAt(i, 1).toString()).getId());
         td.setQty(Integer.parseInt(model.getValueAt(i, 4).toString()));
         td.setPrice(Double.parseDouble(model.getValueAt(i, 6).toString()));
         td.setCreatedAt(LocalDateTime.now());
         td.setCreatedBy(1);
         
        
        boolean success = transactionUc.createDetailTransaction(td);
        if (!success) {
            JOptionPane.showMessageDialog(this, "CREATE: " + (success ? "Berhasil" : "Gagal"));
            clearInputFields();
            tItem.removeAll();
            return;
            }     
        }
        
         clearInputFields();
         model.setRowCount(0); // clear data table
        listItems.clear(); 
        
        System.out.println("berhasil!");
         JOptionPane.showMessageDialog(this, "CREATE: " + (true ? "Berhasil" : "Gagal"));
    }//GEN-LAST:event_btnSaveActionPerformed

